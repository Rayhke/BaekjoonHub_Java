public class Main {
    public static void main(String[] args) {
        System.out.print("Hello World!");
    }
}
/* ■ 설명 ■
[System.out.print] 이랑 [System.out.println] 는
c언어에 [printf("\n");] 에 들어가는 [\n] 와 기능이 같다.
즉, 작동 후 줄바꿈이 되도록 하고자 하면 반드시
[println] 를 사용할 것

[main(String[] args)] 는 매개변수일 때 선언하지만, 왠만하면 넣어둘 것.
*/
